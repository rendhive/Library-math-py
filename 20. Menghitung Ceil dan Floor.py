import math

value = 2.3
ceil_value = math.ceil(value)
floor_value = math.floor(value)
print(f"Ceil dari {value} adalah {ceil_value}.")
print(f"Floor dari {value} adalah {floor_value}.")
# Fungsi: Menghitung nilai ceil (pembulatan ke atas) dan floor (pembulatan ke bawah).
# Kondisi: Ketika Anda perlu membulatkan angka desimal ke angka bulat terdekat.
