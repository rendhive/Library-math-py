import math

sin_value = 0.5
angle_rad = math.asin(sin_value)
angle_deg = math.degrees(angle_rad)
print(f"Sudut dengan sin = {sin_value} adalah {angle_deg} derajat.")
# Fungsi: Menghitung sudut (dalam radian) dari nilai sinus.
# Kondisi: Ketika Anda ingin menemukan sudut berdasarkan nilai sinusnya.
