import math

angle_rad = math.radians(450)  # 450 derajat lebih dari 360
cos_value_large = math.cos(angle_rad)
print("Cos 450 derajat:", cos_value_large)
# Fungsi: Menghitung nilai kosinus untuk sudut lebih dari 360 derajat.
# Kondisi: Ketika Anda mencoba menghitung fungsi trigonometri di luar rentang 0-360 derajat.
